class BERT_SPC(nn.Module):
    def __init__(self, bert, opt):
        super(BERT_SPC, self).__init__()
        # self.bert = Rationale_Bert.from_pretrained('bert-base-uncased')
        self.bert = bert
        self.dropout = nn.Dropout(opt.dropout)
        self.dense = nn.Linear(opt.bert_dim, opt.polarities_dim)

    def forward(self, inputs,rational_mask):
        text_bert_indices, bert_segments_ids,attention_mask = inputs[0], inputs[1], inputs[2]
        # res = self.bert(text_bert_indices, attention_mask=attention_mask,token_type_ids=bert_segments_ids, rationale_mask = rational_mask )
        res = self.bert(text_bert_indices, token_type_ids=bert_segments_ids, rationale_mask = rational_mask )
        pooled_output = res.pooler_output
        pooled_output = self.dropout(pooled_output)
        logits = self.dense(pooled_output)
        return logits
    
